# division for even numbers
float_division = 10 / 2
print(float_division)

# division for odd numbers
float_division = 11 / 2
print(float_division)

# division dapat whole number
integer_division = 10 // 2
print(integer_division)

# division dapat whole number tpi xdibundarkan
integer_division = 11 // 2
print(integer_division)

remainder = 13 % 2
print(remainder)
